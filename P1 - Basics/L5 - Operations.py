# Bitwise Operator
''' convert number in binary ; do bitwise operation
    then again converts in decinmal '''

print(a & b)            # AND gate
print(a | b)            # OR gate
print(~a)               # NOT gate
print(a^b)              # XOR gate

print(a<<2)             # append 2 zero at last  (left shift)
print(a>>2)             # remove 2 digits from last  (right shift)


# Membership Operators

print(a in list)          # True
print(a not in list)      # False


# Identity Operator
''' Both the values are of same type and same value
              True/False          '''

print((2==2) is True)     # True
print(4 is True)           # False
